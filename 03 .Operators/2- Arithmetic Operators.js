/**Arithmetic
 * Addition +
 * Subtraction -
 * Multiplication *
 * Division /
 * Remainder of division %
 * Exponent ** الاس التربيعى
 * Increment ++
 * Decrement --
 */

let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

//Increment ++
console.log(++x); //  increase by 1 then show x  = 11
console.log(x++); // show x first then increase by 1 = 10
//Decrement --
console.log(--x); //  decrease by 1 then show x  = 9
console.log(x--); // show x first then decrease by 1 = 10
