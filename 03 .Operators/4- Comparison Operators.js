/**Comparison
 * to run true/false tests
 *  === Strict equality
 *  !== Strict-non-equality
 *  <   Less than
 *  >   Greater than
 *  <=  Less than or equal to
 *  >=  Greater than or equal to
 */

let x = 1;

// Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

// Equality
console.log(x === 1);
console.log(x !== 1);
