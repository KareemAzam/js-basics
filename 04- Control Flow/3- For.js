/** For
 * Definition
 *  For(initialExpression; Condition;incrementExpression)
 *
 * for (let i = 0; i < 5; )
 * {statement}
 */

for (let i = 0; i < 5; i++) {
  console.log("hello World", i);
}

// display the odd number between 1 to 5
for (let i = 0; i <= 5; i++) {
  if (i % 2 !== 0) console.log(i);
}
